                  <p>In Ayurveda, excessive hair fall is known as Khalitya and is primarily caused by excess Pitta dosha affecting the hair follicles. Pitta, being hot and sharp, destroys hair tissue when aggravated. Vata imbalance contributes by causing dryness and poor scalp circulation. Treatment focuses on cooling and balancing Pitta, nourishing hair roots (Keshya), improving scalp blood flow, and reducing stress-related hair loss.</p>

