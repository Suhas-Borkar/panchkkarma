                  <h3 class="mb-2">What Is Paralysis in Ayurveda?</h3>
                  <p>In Ayurveda, paralysis is known as Pakshaghata and is caused by extreme Vata dosha aggravation blocking the nervous system pathways (Vatavaha Srotas). This leads to loss of sensation and motor function. Treatment focuses on pacifying Vata dosha, nourishing nerve tissues (Majja Dhatu), clearing blocked channels, and progressively restoring neurological function.</p>

                  <h3 class="mb-2 mt-4">Causes of Paralysis</h3>
                  <ul class="list-style-one mb-3">
