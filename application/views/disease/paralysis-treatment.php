                    <li>Stroke (cerebrovascular accident) or TIA</li>
                    <li>Spinal cord injury or nerve compression</li>
                    <li>Brain trauma or neurological conditions</li>
                    <li>Severe depletion of nerve tissues (Dhatu Kshaya)</li>
                    <li>Toxin accumulation blocking neurological channels</li>
                    <li>Bell's palsy or facial nerve disorders</li>
                  </ul>

                  <h3 class="mb-2 mt-4">Signs and Symptoms</h3>
