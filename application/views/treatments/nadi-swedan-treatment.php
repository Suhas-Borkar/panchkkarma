                  <h3 class="mb-2">What Is Nadi Swedan?</h3>
                  <p>Nadi Swedan is a targeted steam therapy where warm herbal steam prepared from medicated decoctions is directed through a hose or steam box to the affected area. The heat and herbal vapors penetrate tissues, loosen muscle tension, improve circulation, and prepare the body for Panchakarma procedures.</p>

                  <h3 class="mb-2 mt-4">How Nadi Swedan Works in Ayurveda</h3>
                  <p>Ayurveda links stiffness, heaviness, and toxin accumulation to imbalances in Vata and Kapha and the presence of Ama. The warm herbal steam of Nadi Swedan penetrates deep into tissues to liquefy and mobilize Ama, improve blood flow, and pacify Vata and Kapha, supporting detox and pain relief.</p>

                  <h3 class="mb-2 mt-4">Procedure</h3>
                  <ul class="list-style-one mb-3">
                    <li>A medicated herbal decoction is prepared and heated to produce steam.</li>
                    <li>One end of a hose/pipe or a steam box is attached to the decoction cooker; the other end is positioned near the local body part or over a steam enclosure.</li>
                    <li>The patient is comfortably positioned and the warm herbal steam is applied to the target area for a prescribed duration.</li>
                    <li>The therapist monitors temperature and comfort, replenishing decoction and adjusting duration as needed.</li>
                    <li>After the session the area is dried and gentle post-care is advised; the therapy is often used before or after Abhyanga, Basti, or other Panchakarma treatments.</li>
                  </ul>

