                  <h3 class="mb-2">What Is Navarakizhi Treatment?</h3>
                  <p>Navarakizhi involves gentle, rhythmic massage of the body using warm potalies (bundles) of Navara rice cooked in medicated milk and herbal decoctions. The sustained warmth and herbal nourishment penetrate the muscles and joints, promoting rejuvenation and tissue strengthening.</p>

                  <h3 class="mb-2 mt-4">How Navarakizhi Works in Ayurveda</h3>
                  <p>Ayurveda views Navarakizhi as a nourishing and strengthening therapy that counteracts Vata-related degeneration and poor tissue nutrition. The herbal milk and rice deliver continuous heat and medicated nourishment, improving circulation, supporting tissue repair, and balancing aggravated Vata.</p>

                  <h3 class="mb-2 mt-4">Procedure</h3>
                  <ul class="list-style-one mb-3">
                    <li>Prepare the medicated liquid (kadha) using milk and classical herbal decoctions.</li>
                    <li>Process Shashtishali/Navara rice in half the kadha to make warm potalies (rice bundles).</li>
                    <li>Keep the remaining kadha heated and dip the potalies as needed during the procedure.</li>
                    <li>Therapist performs rhythmic massage over full or selected parts of the body using alternate potalies; duration and pressure are tailored per patient.</li>
                  </ul>

