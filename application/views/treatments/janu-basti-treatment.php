              <h3 class="mt-4 mb-2">Ayurvedic Treatment for Knee Pain – Janu Basti Therapy</h3>
              <p><strong>Janu Basti</strong> is a traditional Ayurvedic treatment for knee pain in which warm medicated oil is retained over the knee joint using a specially crafted black gram dough ring. The therapy deeply nourishes knee tissues, strengthens the joint, reduces inflammation, and restores natural mobility.</p>
              <p>The term Janu Basti comes from two Sanskrit words: Janu, meaning knee, and Basti, meaning to retain or hold. Ayurveda recommends this therapy to pacify aggravated Vata dosha, improve lubrication of the knee joint, and provide lasting relief from chronic knee pain and stiffness.</p>

