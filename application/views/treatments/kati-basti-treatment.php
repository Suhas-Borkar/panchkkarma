              <h3 class="mt-4 mb-2">Ayurvedic Treatment for Lower Back Pain – Kati Basti Therapy</h3>
              <p><strong>Kati Basti</strong> is a traditional Ayurvedic treatment for lower back pain in which warm medicated oil is retained over the lumbar region using a specially formed black gram dough ring. The therapy deeply penetrates the lower back to reduce pain, relieve stiffness, nourish spinal discs, and improve lumbar strength.</p>
              <p>The term Kati Basti comes from two Sanskrit words: Kati, meaning waist or lower back, and Basti, meaning to retain. Ayurveda recommends this therapy to pacify aggravated Vata dosha in the lumbar region, support spinal disc health, and provide lasting relief from chronic lower back pain.</p>

