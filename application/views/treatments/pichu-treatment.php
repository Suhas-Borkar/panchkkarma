              <h3 class="mt-4 mb-2">Pichu Treatment - Ayurvedic Oil Therapy</h3>
              <p><strong>Pichu</strong> is a traditional Ayurvedic oil therapy in which a cotton or cloth pad soaked in warm medicated oil is placed over an affected area such as the head, lower back, neck, or joints. The therapy helps relieve pain, stiffness, inflammation, nerve irritation, and Vata-related discomfort.</p>
              <p>This gentle therapy offers continuous warmth and nourishment to the selected area. It is commonly used for calming the nerves, supporting healing, reducing stress-related discomfort, and managing chronic pain conditions in a safe, non-invasive way.</p>

