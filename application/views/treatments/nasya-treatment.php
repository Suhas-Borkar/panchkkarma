                  <h3 class="mb-2">What Is Nasya Treatment?</h3>
                  <p>Nasya involves the gentle instillation of warm medicated oil or herbal preparations into the nostrils by a trained practitioner. The treatment helps remove toxins from the head area, lubricate the nasal passages, improve breathing, and support sinus and ENT health.</p>

                  <h3 class="mb-2 mt-4">How Nasya Works in Ayurveda</h3>
                  <p>In Ayurveda the nose is considered the gateway to the head. Imbalance of Vata and Kapha in this region leads to congestion, headaches, and neurological symptoms. Nasya clears excess mucus and Ama, nourishes tissues, improves circulation in the head and neck, and helps restore dosha balance.</p>

                  <h3 class="mb-2 mt-4">Procedure</h3>
                  <ul class="list-style-one mb-3">
                    <li>Patient receives a facial and neck massage followed by gentle steam to open passages.</li>
                    <li>With the patient lying comfortably and neck extended, the practitioner pours the doctor-prescribed warm Nasya medicine into each nostril.</li>
                    <li>The patient rests for 5–10 minutes allowing the medicine to act and the loosened mucus to drain.</li>
                    <li>Patient may be asked to spit out any discharged mucus; therapist supervises and provides post-care instructions.</li>
                  </ul>

