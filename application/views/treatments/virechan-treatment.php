              <h3 class="mt-4 mb-2">Virechan Treatment – Ayurvedic Cleansing &amp; Detox Therapy</h3>
              <p><strong>Virechan</strong> is a core Panchakarma detox therapy in Ayurveda that uses medicated herbal purgatives to eliminate excess Pitta dosha and accumulated metabolic toxins (Ama) from the body through the lower digestive tract. It is one of the five primary Panchakarma procedures and is considered the most effective treatment for Pitta-related disorders.</p>
              <p>Unlike Vaman which targets Kapha dosha through the upper body, Virechan focuses on Pitta elimination through the intestinal tract, cleansing the liver, gallbladder, small intestine, and blood — all primary seats of Pitta dosha in the body.</p>

