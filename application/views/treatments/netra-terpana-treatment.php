                  <p>The universe is viewed through the eyes. Eye protection is crucial, and corrective lenses alone are not enough. Ayurveda recommends preventive eye maintenance, which is accomplished through gentle hydration of the eyes with Ayurvedic ghrita.</p>

                  <h3 class="mb-2 mt-4">Procedure</h3>
                  <ul class="list-style-one mb-3">
                    <li>The compartment of black gram flour is prepared around both eyes.</li>
                    <li>The lukewarm medicated ghrita is poured into the prepared compartment until it reaches the eyelashes.</li>
                    <li>The patient is asked to blink gently several times while the ghee remains in place.</li>
                    <li>At the conclusion of the procedure, the ring is removed and the remaining ghee is squeezed out.</li>
                    <li>A light massage may be given around the affected area, followed by gentle cleansing with lukewarm water or Triphala kwath.</li>
                  </ul>

